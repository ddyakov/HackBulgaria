using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace AnonymousTypesAndNestedClasses
{
    public class Book
    {
        private string name;
        private int id;

        public string Name
        {
            get
            {
                return this.name;
            }
        }

        public int Id
        {
            get
            {
                return this.id;
            }
        }
    }
}
